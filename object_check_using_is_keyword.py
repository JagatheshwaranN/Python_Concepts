# In python, we can compare two objects using IS keyword.

a = 10.234
b = 10.234
print(a is b)  # True ( Both variables using the same object only)

c = True
d = True
print(c is d)  # True ( Both variables using the same object only)

e = "code snippet"
f = "code snippet"
print(e is f)  # True ( Both variables using the same object only)

g = 10+20j
h = 10+20j
print(g is h)  # True ( Both variables using the same object only)
