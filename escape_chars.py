# \n -> New Line
# \t -> Horizontal Tab
# \r -> Carriage Return
# \b -> Back Space
# \f -> Form Feed
# \' -> Single Quote
# \"-> Double Quote
# \\-> Back Slash

print('python program')
print('python\tprogram')
print('python\nprogram')
print('This is \'symbol\' of gratitude')
print('This is \"symbol\" of courage')
print('python\rlanguage')
print('python\flanguage')
print('This is \\python\\ course')
